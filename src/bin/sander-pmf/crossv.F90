   subroutine crossv ( a, b, c ) 

   implicit none

   _REAL_ , intent(in ) :: a(3), b(3)
   _REAL_ , intent(out) :: c(3)

!  +---------------------------------------------------------------------------+
!  |  C = A X B                                                                |
!  +---------------------------------------------------------------------------+

   c(1) = a(2) * b(3) - a(3) * b(2)
   c(2) = a(3) * b(1) - a(1) * b(3)
   c(3) = a(1) * b(2) - a(2) * b(1)

